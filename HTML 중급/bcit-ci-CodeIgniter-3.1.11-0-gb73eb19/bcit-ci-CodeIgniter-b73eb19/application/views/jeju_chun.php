				이 숲에 자생하는 담팔수나무는 이곳이 그 북방한계선으로써 천연기념물 163호로 지정되었고, 난대림 자체도 천연기념물 379호로 지정, 보호되고 있어, 벌목, 식물채집, 야생동물 포획이 엄격히 금해진다.<br>
				폭포가 떨어지는 깊이 20m의 못 속에는 무태장어가 서식하는 것으로도 유명한데, 무태장어는 바다에서 산란하고 하천이나 호수로 돌아오는 회유성 어류이다. <br>
				낮에는 하천이나 호수의 깊은 곳에 숨어 있다가 밤에는 얕은 곳으로 나와 먹이를 잡아먹는데, 큰 것은 길이가 2m, 무게가 20㎏에 이른다.<br>
				천지연 폭포가 무태장어 서식분포의 북방한계선이기 때문에 천연기념물 27호로 지정되었다.<br>
				이쯤이면 천지연폭포는 '천연기념물의 보고'라고 부를 법하다.<br>
				산책로 끝에 위치한 천지연폭포는 오후 10시까지 야간개장을 하는데, 밤에 보는 폭포의 모습도 장관이다.<br>

			</p>
